	public void carregarComboCNPJ() {

		FornecedorDAO fornecedorDAO = new FornecedorDAO();
		List<Fornecedor> list = fornecedorDAO.retrieve();
		for (Fornecedor item : list) {
			jComboBoxFornecedor.addItem(item.getCnpjFornecedor());
		}

	}

